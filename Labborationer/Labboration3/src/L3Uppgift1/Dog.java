package L3Uppgift1;

public class Dog {
    private int age;
    private String gender;
    private String name;

    public void Dog(){
    }
    public int getAge(){
        return this.age;
    }
    public int setAge(int age){
        this.age = age;
        return this.age;
    }
    public String getGender(){
        return this.gender;
    }
    public String setGender(String gender){
        this.gender = gender;
        return this.gender;
    }
    public String getName(){
        return this.name;
    }
    public String setName(String name){
        this.name = name;
        return this.name;
    }
}
